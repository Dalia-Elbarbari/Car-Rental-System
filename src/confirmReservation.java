    private void proceedPaymActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedPaymActionPerformed

    if (customerField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter customer Name!");
        return;
    }

    customerController controller = new customerController();
    
    
    String cleanPrice = totalCostField.getText().replace("EGP", "").trim();
    double total = Double.parseDouble(cleanPrice);

   
    boolean success = controller.makeReservation(
        customerField.getText(), 
        this.finalStartDate, 
        this.finalEndDate,        
        total,
        statusField.getText()
    );

    if (success) {
        JOptionPane.showMessageDialog(null, "Reservation Saved Successfully! Opening Payment...");
        
    
        Reservation res = new Reservation();
        res.setTotalCost(total);
        res.setReservationId(customerField.getText()); 
        
        PaymentUI paymentScreen = new PaymentUI(res); 
        paymentScreen.setVisible(true);

        this.dispose(); // إ
        
    } else {
        JOptionPane.showMessageDialog(null, "Error: Could not save to Database.");
    }
    }//GEN-LAST:event_proceedPaymActionPerformed

