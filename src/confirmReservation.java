    private void cancellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancellActionPerformed
     // Display a confirmation dialog before canceling
    int response = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to cancel this reservation?", 
            "Confirm Cancellation", 
            javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (response == javax.swing.JOptionPane.YES_OPTION) {
        // Return to the main search screen and close this window
        // new ReservationGUI().setVisible(true);
        this.dispose();
    }
    }//GEN-LAST:event_cancellActionPerformed

