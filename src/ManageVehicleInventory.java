private void refreshTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) vehicleTable.getModel();
        model.setRowCount(0); 
        
        java.util.List<Vehicle> list = controller.getInventory().getAllVehicles();
        
        for (Vehicle v : list) {
            Object[] row = { 
                v.getVehicleID(), 
                v.getModel(), 
                v.getDailyRate(), 
            };
            model.addRow(row);
        }
    }

    private void clearFields() {
        txtId.setText("");
        txtModel.setText("");
        txtRate.setText("");
        
    }
