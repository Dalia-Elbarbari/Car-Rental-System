    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String selectedStatus = jTextField2.getText().trim();
            String id = txtId.getText().trim();
            String modelName = txtModel.getText().trim();
            String rateStr = txtRate.getText().trim();
            

            if (id.isEmpty() || modelName.isEmpty() || rateStr.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please fill required fields!");
                return;
            }

           
            Vehicle v = new Vehicle();
            v.setVehicleID(id);
            v.setModel(modelName);
            v.setDailyRate(Double.parseDouble(rateStr));
            
        
            try {
                v.setStatus(VehicleStatus.valueOf(selectedStatus)); 
            } catch (Exception e) {
                v.setStatus(VehicleStatus.Available); 
            }

            controller.addVehicle(v);
            
          
            refreshTable();
            clearFields(); 
            javax.swing.JOptionPane.showMessageDialog(this, "Vehicle Added Successfully!");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Check Daily Rate format!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

